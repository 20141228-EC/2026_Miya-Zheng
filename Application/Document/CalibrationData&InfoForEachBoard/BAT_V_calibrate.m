% 示例数据（替换为你的实际数据）
x = [...
24.03;...
23.97;...
23.92;...
23.88;...
23.83;...

];    % 第一列数据（列向量）
y = [...
1.29;...
2.14;...
2.98;...
3.83;...
4.69;...


];    % 第二列数据（列向量）

% 构建设计矩阵和右侧向量
M = [x, y];
rhs = 24 * ones(length(x), 1);

% 求解最小二乘解
coeff = M \ rhs;      % 结果：[a; b]
a = coeff(1)
b = coeff(2)

% 检查残差（可选）
residual = M * coeff - rhs;
RMSE = sqrt(mean(residual.^2));
disp(['拟合残差: ', num2str(RMSE)]);
